    /**
     * Sets the netCDF data.
     * 
     * @param fileName
     *           the name of data file
     * @param ncFile
     *           the netCDF data
     * @param dataType
     *            type of data
     * @param colInfoSet
     *            data columns
     * @param infoMap
     *            a map of information
     * @param showDefault
     *            a flag whether to show default column type
     * @return true if succeeded
     */
    public boolean setData(String fileName, SGNetCDFFile ncFile, String dataType,
    		SGDataColumnInfoSet colInfoSet,
            Map<String, Object> infoMap, final boolean showDefault) {
        
        // set the data type and information map
        this.setDataType(dataType, infoMap);

        // set the file name
        this.setFileName(fileName);
        
        if (this.mNetCDFDataSetupPanel.setData(ncFile, dataType, colInfoSet,
        		infoMap, showDefault) == false) {
            return false;
        }
        
        return true;
    }

    /**
     * Set information of data columns.
     * 
     * @param fileName
     *           the name of data file
     * @param data
     *            netCDF data
     * @param colInfoSet
     *            data columns
     * @param infoMap
     *            a map of information
     * @param showDefault
     *            a flag whether to show default column type
     * @return true if succeeded
     */
    public boolean setData(String fileName, SGNetCDFData data, 
            SGDataColumnInfoSet colInfoSet,
            Map<String, Object> infoMap, final boolean showDefault) {

        // set data
        if (this.setData(fileName, data.getNetcdfFile(), data.getDataType(), 
        		colInfoSet, infoMap, showDefault) == false) {
            return false;
        }

        return true;
    }
    
