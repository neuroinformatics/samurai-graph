    /**
     * Set information of data columns.
     *
     * @param data
     *            multidimensional data
     * @param colInfoSet
     *            data columns
     * @param infoMap
     *            a map of information
     * @param showDefault
     *            a flag whether to show default column type
     * @return true if succeeded
     */
    public boolean setData(SGMDArrayData data,
            SGDataColumnInfoSet colInfoSet,
            Map<String, Object> infoMap, final boolean showDefault) {
    	
    	// set the title
    	this.setupTitle(data.getDataType());
    	
    	// set to the panel
        if (this.mMDArrayDataSetupPanel.setData(data, colInfoSet, infoMap,
                showDefault) == false) {
        	return false;
        }
        this.pack();
        
        return true;
    }

