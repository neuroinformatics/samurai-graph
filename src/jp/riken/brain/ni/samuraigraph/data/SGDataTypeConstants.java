    /**
     * Returns a text string that represents the data type of the long form.
     * 
     * @param dataType
     *                a text string that represents the data type of the short form
     * @return a text string that represents the data type of the long form
     */
    public static final String getLongDataTypeConstant(final String dataType) {
        String ret = null;
        for (int ii = 0; ii < DATA_TYPE_ARRAY.length; ii++) {
            if (DATA_TYPE_ARRAY[ii][0].equals(dataType)) {
                return DATA_TYPE_ARRAY[ii][1];
            }
        }
        return ret;
    }
